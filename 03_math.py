import  math

y,z = 2,3;
x=2.245
# rounds off the number
print(round(x))
# add power to that numeber
print(pow(x,2))
# gives the upper limit of that number
print(math.ceil(x))
# gives the lower limit of that number
print(math.floor(x))
# finds the square root of the numebr
print(math.sqrt(x))
# finds the max number between multiple number
print(max(x,y,z))
# finds the min number between multiple number
print(min(y,z))